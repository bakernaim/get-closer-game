// Build cards for a single topic
export function buildTopicCards(topicId) {
  const topic = TOPICS[topicId]
  if (!topic) return []
  return topic.questions.map((question, index) => ({
    id: `${topicId}-${index}`,
    topicId,
    topicName: topic.name,
    topicColor: topic.color,
    topicColorDark: topic.colorDark,
    topicColorText: topic.colorText,
    topicIcon: topic.icon,
    question,
  }))
}

// Build per-topic deck map (used by new state structure)
export function buildPerTopicDecks(selectedTopicIds) {
  return Object.fromEntries(
    selectedTopicIds
      .filter(id => TOPICS[id])
      .map(id => [id, buildTopicCards(id)])
  )
}

// Legacy flat deck builder (kept for compatibility)
export function buildDeck(selectedTopicIds) {
  return selectedTopicIds.flatMap(buildTopicCards)
}

